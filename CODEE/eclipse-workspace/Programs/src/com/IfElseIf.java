package com;

public class IfElseIf {
	public static void main(String[] args) {
		int m = 4;
		int n = 5;
		if(m<n) {
			System.out.println(m+" is lesser than "+n);
		}
		else if(m>n) {
			System.out.println(m+" is greater than "+n);
		}
		else {
			System.out.println(m+" is equal to "+n);
			
		}

	}

}
